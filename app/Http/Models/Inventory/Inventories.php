    public static function stockMovement($params, $type)
    {
        $data = self::where('id', $params['inventory_id']);

        if ($type == 'out') {
            $data->decrement('stock', $params['qty']);
        } else if ($type == 'in') {
            $data->increment('stock', $params['qty']);
        }
    }
